print("Python")
print()

# Для добавления в текст табуляции используется комбинация символов \t:
print("\tPython") 
print()

# Разрывы строк добавляются с помощью комбинации символов \n:
print("Languages:\nPython\nC\nJavaScript")
print()
# В следующем примере показано, как использо-вать 
# одну строку кода для создания четырех строк вывода:

print("Languages:\n\tPython\n\tC\n\tJavaScript")
print()